    private void rSButtonIconOne1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonIconOne1ActionPerformed
        model.Devolucion d = new model.Devolucion();
        validation.Validacion_Devolucion v = new validation.Validacion_Devolucion();
        d.setNombre_estudiante(this.txtNombre.getText());
        d.setApellido_estudiante(this.txtApellido.getText());
        d.setDni_estudiante(this.txtDni.getText());
        d.setCurso_estudiante(this.txtCurso.getText());
        d.setDivision_estudiante(this.txtDivision.getText());
        d.setTitulo_libro(this.txtTitulo.getText());
        d.setEdicion_libro(this.txtEdicion.getText());
        d.setIsbn_libro(this.txtIsbn.getText());
        d.setAutor_libro(this.txtAutor.getText());
        d.setEditorial_libro(this.txtEditorial.getText());
        java.util.Date fecha = this.dtDevolucion.getDatoFecha();
        if (v.validarDevolucionEstudiante(this.txtNombre, this.txtApellido, this.txtDni, this.txtCurso, this.txtDivision, this.lbErrorEstudiante) == true
                && v.validarDevolucionLibro(this.txtTitulo, this.txtEdicion, this.txtIsbn, this.txtAutor, this.txtEditorial, this.lbErrorLibro) == true
                && v.validarBuscarEstudiante(this.txtBuscarDni, this.lbErrorFecha) == true
                && v.validarBuscarLibro(this.txtBuscarIsbn, this.lbErrorFecha) == true
                && v.validarFechaDevolucion(this.dtDevolucion, this.lbErrorFecha) == true) {
            java.sql.Date fechaSql = new java.sql.Date(fecha.getTime());
            d.setFecha_devolucion(fechaSql);
            if (bbdd.Consulta_Devolucion.registrarDevolucion(d)) {
                bbdd.Consulta_Devolucion.listarDevolucion("");
                this.lbErrorEstudiante.setText("");
                this.lbErrorLibro.setText("");
                this.lbErrorFecha.setText("");
                this.txtNombre.setText("");
                this.txtApellido.setText("");
                this.txtDni.setText("");
                this.txtCurso.setText("");
                this.txtDivision.setText("");
                this.txtTitulo.setText("");
                this.txtEdicion.setText("");
                this.txtIsbn.setText("");
                this.txtAutor.setText("");
                this.txtEditorial.setText("");
                SuccessAlert sa = new SuccessAlert();
                sa.setVisible(true);
                sa.setLocationRelativeTo(null);
                sa.setResizable(false);
            } else {
                DangerAlert da = new DangerAlert();
                da.setVisible(true);
                da.setLocationRelativeTo(null);
                da.setResizable(false);
            }
        }
    }//GEN-LAST:event_rSButtonIconOne1ActionPerformed

