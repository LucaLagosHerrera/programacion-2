    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        model.Libro l = new model.Libro();
        validation.Validacion_Libro v = new validation.Validacion_Libro();
        l.setIsbn(this.txtIsbn.getText());
        if (v.validarLibro(this.txtTitulo, this.txtEdicion, this.txtIsbn, this.txtAutor, this.txtEditorial, this.lbError) == true) {
            if (bbdd.Consulta_Libro.buscarLibroEliminar(l, this.lbError) == true) {
                if (JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar este libro?", "MENSAJE", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    if (bbdd.Consulta_Libro.eliminarLibro(l)) {
                        bbdd.Consulta_Libro.listarLibro("");
                        this.txtTitulo.setText("");
                        this.txtEdicion.setText("");
                        this.txtIsbn.setText("");
                        this.txtAutor.setText("");
                        this.txtEditorial.setText("");
                        this.lbError.setText("");
                        SuccessAlert sa = new SuccessAlert();
                        sa.setVisible(true);
                        sa.setLocationRelativeTo(null);
                        sa.setResizable(false);
                    } else {
                        DangerAlert da = new DangerAlert();
                        da.setVisible(true);
                        da.setLocationRelativeTo(null);
                        da.setResizable(false);
                    }
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

