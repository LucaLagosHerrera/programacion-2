    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        model.Libro l = new model.Libro();
        validation.Validacion_Libro v = new validation.Validacion_Libro();
        l.setTitulo(this.txtTitulo.getText());
        l.setEdicion(this.txtEdicion.getText());
        l.setIsbn(this.txtIsbn.getText());
        l.setAutor(this.txtAutor.getText());
        l.setEditorial(this.txtEditorial.getText());
        if (v.validarLibro(this.txtTitulo, this.txtEdicion, this.txtIsbn, this.txtAutor, this.txtEditorial, this.lbError) == true) {
            if (bbdd.Consulta_Libro.buscarLibroRegistrado(l, this.lbError) == false) {
                bbdd.Consulta_Libro.listarLibro("");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

